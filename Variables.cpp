#include <iostream>

using namespace std;

int main()
{

    string characterName = "John";
    
    int characterAge;
    characterAge = 35;
    
    cout << "\nThere once was a man named "<< characterName << endl;
    cout << "He was " << characterAge << " years old" << endl;
    
    characterName = "Mike";
    characterAge = 50;
    
    cout << "He liked the name " << characterName << endl;
    cout << "But did not like being " << characterAge << "." << endl;
    
    return 0;
}
